"""LLM-judged evaluations for edge case handling.

Tests whether the analysis correctly handles apps with missing features:
- No database, no auth (static landing page)
- AI-heavy app (should have deep AI section)
Run: uv run pytest evals/test_edge_cases.py -v -m eval
"""

from __future__ import annotations

import sys
from pathlib import Path

import pytest
from deepeval import assert_test
from deepeval.test_case import LLMTestCase

_src = Path(__file__).resolve().parent.parent / "src"
if str(_src) not in sys.path:
    sys.path.insert(0, str(_src))

from conftest import (
    edge_case_ai_depth_metric,
    edge_case_no_hallucination_metric,
)
from fixtures import (
    SAMPLE_AI_HEAVY_FILES,
    SAMPLE_NO_DB_NO_AUTH_ANALYSIS,
    SAMPLE_NO_DB_NO_AUTH_FILES,
)


@pytest.mark.eval
class TestNoDbNoAuthApp:
    """Does the analysis correctly identify absent features without hallucinating?"""

    def test_no_hallucination_of_missing_features(self):
        test_case = LLMTestCase(
            input=SAMPLE_NO_DB_NO_AUTH_FILES,
            actual_output=SAMPLE_NO_DB_NO_AUTH_ANALYSIS,
        )
        assert_test(test_case, [edge_case_no_hallucination_metric])


@pytest.mark.eval
class TestAiHeavyApp:
    """Does the analysis provide deep coverage of AI/LLM integration?"""

    def test_ai_section_depth(self):
        # For this test we need an analysis of the AI-heavy app.
        # We use the source files as input and a sample analysis as output.
        # Since we don't have a pre-generated analysis, we test the source
        # files against the edge_case_ai_depth_metric which checks that
        # an analysis WOULD cover AI properly.
        #
        # In a real pipeline run, we'd invoke the analyst agent.
        # Here we provide a minimal AI-focused analysis.
        ai_analysis = """\
## Technical Analysis: ai-chat

### 1. Purpose
An AI-powered chat application that allows users to have conversations with GPT-4o, \
with message persistence and semantic document search via embeddings.

### 2. Tech Stack
- **Next.js 15.0.0** — React framework with App Router
- **ai 4.0.0** — Vercel AI SDK for streaming LLM responses
- **@ai-sdk/openai 1.0.0** — OpenAI provider for the Vercel AI SDK
- **drizzle-orm 0.38.0** — type-safe SQL ORM
- **@neondatabase/serverless 0.10.0** — serverless Postgres driver
- **zod 3.23.0** — schema validation

### 3. Data Flow
1. User sends a message via the chat interface
2. `POST /api/chat` receives `{ prompt, conversationId }`
3. Message is persisted to `messages` table via `db.insert(messages).values()`
4. `streamText()` from Vercel AI SDK calls OpenAI GPT-4o with system prompt
5. Streaming response returned via `result.toDataStreamResponse()`
6. For search: `findSimilarDocuments()` generates query embedding via `embed()` with \
`text-embedding-3-small`, then queries `documents` table ordered by `cosineDistance()`

### 4. Architecture
- Next.js App Router with streaming API route at `/api/chat`
- Vercel AI SDK abstracts LLM provider behind a unified interface
- Embedding-based retrieval in `lib/embeddings.ts` using cosine similarity

### 5. Features
- Real-time streaming chat with GPT-4o via `streamText()`
- Message persistence in PostgreSQL
- Semantic document search via `findSimilarDocuments()`
- Vector embeddings generated by `text-embedding-3-small` model

### 6. AI / LLM Integration
- **Chat model**: `openai("gpt-4o")` via `@ai-sdk/openai` provider
- **System prompt**: `"You are a helpful coding assistant."`
- **Streaming**: `streamText()` returns a `DataStreamResponse` for real-time token delivery
- **Embeddings**: `openai.embedding("text-embedding-3-small")` via `embed()` function
- **Retrieval**: `findSimilarDocuments()` in `lib/embeddings.ts` — generates query embedding, \
then orders `documents` table by `cosineDistance(documents.embedding, queryEmbedding)` with \
configurable `limit` (default 5)
- **Embedding generation**: `generateEmbedding()` wraps the `embed()` call for reuse

### 7. Database & Schema
- **messages**: stores chat history with `conversationId`, `role`, `content`
- **documents**: stores embeddings for semantic search with `embedding` vector column

### 8. API Design
- `POST /api/chat` — accepts `{ prompt, conversationId }`, persists user message, \
streams GPT-4o response

### 9. Auth & Security
No authentication detected in the provided source files.

### 10. Unique Patterns
- Combines streaming chat and embedding-based retrieval in a single app
- Uses Vercel AI SDK's `streamText` + `toDataStreamResponse` for efficient token streaming
- `cosineDistance` from drizzle-orm enables vector similarity search directly in SQL
"""
        test_case = LLMTestCase(
            input=SAMPLE_AI_HEAVY_FILES,
            actual_output=ai_analysis,
        )
        assert_test(test_case, [edge_case_ai_depth_metric])
